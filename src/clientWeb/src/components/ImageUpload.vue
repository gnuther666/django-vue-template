<template>
  <el-upload
    class="avatar-uploader"
    :headers="handers"
    :action="`${ServerUrl}${image_url}`"
    :show-file-list="false"
    :on-success="handleAvatarSuccess"
    :before-upload="beforeAvatarUpload"
  >
    <img v-if="imageUrl" :src="imageUrl" class="avatar" />
    <el-icon v-else class="avatar-uploader-icon"><Plus /></el-icon>
  </el-upload>
</template>

<script lang="ts" setup>
import { ref } from 'vue'
import { ElMessage } from 'element-plus'
import { Plus } from '@element-plus/icons-vue'
import { ServerUrl } from '@/http.js'
import type { UploadProps } from 'element-plus'
defineProps(['image_url'])
const imageUrl = ref('')
const handers = ref({ Authorization: 'Bearer ' + localStorage.getItem('access_token') })
const handleAvatarSuccess: UploadProps['onSuccess'] = (response, uploadFile) => {
  imageUrl.value = URL.createObjectURL(uploadFile.raw!)
}

const beforeAvatarUpload: UploadProps['beforeUpload'] = (rawFile) => {
  const accept_image_types = ['image/jpeg', 'image/png']
  if (!accept_image_types.includes(rawFile.type)) {
    ElMessage.error('Avatar picture must be JPG format!' + rawFile.type)
    return false
  } else if (rawFile.size / 1024 / 1024 > 2) {
    ElMessage.error('Avatar picture size can not exceed 2MB!')
    return false
  }
  return true
}
</script>

<style scoped>
.avatar-uploader .avatar {
  width: 100%;
  height: 100%;
  display: block;
}
</style>

